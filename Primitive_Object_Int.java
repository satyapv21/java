package com.example.helloworld.HelloWorld;

public class Primitive_Object_Int {

    public static void main(String[] args) {

        // Primitive
        int x=5 ;
        System.out.println(x);

        // Object
        Integer y=5;
        System.out.println(y);

        // final keyword, essentially a constant
        final int Y = 10;
        // Y = 20 gives an error

    }

}
